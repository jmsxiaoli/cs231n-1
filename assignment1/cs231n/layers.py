def svm_loss(x, y):
    """
    Computes the loss and gradient using for multiclass SVM classification.

    Inputs:
    - x: Input data, of shape (N, C) where x[i, j] is the score for the jth
      class for the ith input.
    - y: Vector of labels, of shape (N,) where y[i] is the label for x[i] and
      0 <= y[i] < C

    Returns a tuple of:
    - loss: Scalar giving the loss
    - dx: Gradient of the loss with respect to x
    """
    loss, dx = None, None
    ###########################################################################
    # TODO: Implement loss and gradient for multiclass SVM classification.    #
    # This will be similar to the svm loss vectorized implementation in       #
    # cs231n/classifiers/linear_svm.py.                                       #
    ###########################################################################
    # *****START OF YOUR CODE (DO NOT DELETE/MODIFY THIS LINE)*****

    n = x.shape[0]
    # extract scores corresponding target labels
    target_scores = x[np.arange(n), y].reshape(-1, 1)
    # compute margins with label scores broadcasted
    margins = np.maximum(0, x - target_scores + 1)
    # exclude scores corresponding target labels from loss computation
    margins[np.arange(n), y] = 0
    # compute loss as an average of the sum of margins
    loss = np.sum(margins) / n

    # binary matrix for class grad update
    class_grad = np.where(margins > 0, 1, 0)
    # sum of class grad updates per traing example
    f = class_grad.sum(axis=1)
    # add target class labels grad update
    class_grad[np.arange(n), y] = -f
    # compute grad (see assignment1_sketch for more details)
    dx = class_grad / n

    # *****END OF YOUR CODE (DO NOT DELETE/MODIFY THIS LINE)*****
    ###########################################################################
    #                             END OF YOUR CODE                            #
    ###########################################################################
    return loss, dx


def softmax_loss(x, y):
    """
    Computes the loss and gradient for softmax classification.

    Inputs:
    - x: Input data, of shape (N, C) where x[i, j] is the score for the jth
      class for the ith input.
    - y: Vector of labels, of shape (N,) where y[i] is the label for x[i] and
      0 <= y[i] < C

    Returns a tuple of:
    - loss: Scalar giving the loss
    - dx: Gradient of the loss with respect to x
    """
    loss, dx = None, None
    ###########################################################################
    # TODO: Implement the loss and gradient for softmax classification. This  #
    # will be similar to the softmax loss vectorized implementation in        #
    # cs231n/classifiers/softmax.py.                                          #
    ###########################################################################
    # *****START OF YOUR CODE (DO NOT DELETE/MODIFY THIS LINE)*****

    n = x.shape[0]
    # for numerical stability
    x_stable = x - np.max(x, axis=1).reshape(-1,1)
    # log-probabilities
    P = np.exp(x_stable) / np.sum(np.exp(x_stable), axis=1).reshape(-1,1)
    # extract target scores for computing loss
    target_scores = P[np.arange(n), y]
    # compute loss
    loss = -np.mean(np.log(target_scores))
    # for computing grad corresponding target scores
    P[np.arange(n), y] -= 1
    # compute the gradient
    dx = P / n

    # *****END OF YOUR CODE (DO NOT DELETE/MODIFY THIS LINE)*****
    ###########################################################################
    #                             END OF YOUR CODE                            #
    ###########################################################################
    return loss, dx

